                        <p>PLP Project EHS (Environmental, Health & Safety)’s trainings are conducted through Lectures, Open discussions, Demonstrations / role plays, Video / Audio-visual 
                           aids and Group exercises to enhance the learning experience of our participants. CERTIFICATES that are duly recognized 
                           and approved by the Directorate of Occupational Health and Safety Services shall be given to all course participants 
                           upon successful completion of training.
                           <img class="mb-30" src="https://media.istockphoto.com/id/1166725336/photo/group-of-people-with-instructor-performing-cpr-on-dummy-during-first-aid-training.jpg?s=612x612&w=0&k=20&c=1IB8XcEGZCK3xbSUa6l_hac-CvaMkPKg0Ggxw_13la0=" alt="">
                        </p>                                             
                        <h3>Types Of Training</h3>
                        <div class="card-body">
                        <h4>a. Occupational Safety and Health Committee Training</h4>
                        <p>                        
                            PLP Project EHS (Environmental, Health & Safety) conducts an Occupational Health and Safety training for your Health and Safety committee at your premises or 
                            any other venue that may be agreed upon.The traininig is according to the provisions of the Occupational Safety and 
                            Health Act, 2007 Section 9, and the requirements of Legal Notice No. 31 (Health and Safety Committee Rules) of 2004 
                            as administered by the Department of Occupational Safety and Health Services (DOHSS). The training will be 
                            conducted by our experienced and registered health and safety advisors as per the curriculum(VIEW DOHSS CURRICULUM) 
                            developed by the Directorate of Occupational Health and Safety Services (DOHSS). It is expected that the skills and 
                            expertise the staff will gain in occupational safety and health will greatly assist in developing the workers capacity 
                            (i.e. Improving on their technical skills and knowledge) in organizational safety awareness. The training will also 
                            engender the culture whereby all staff through the trained safety and health committees will take personal responsibility 
                            for safety in all that they do.
                        </p>
                        <h5>Training Objectives</h5>
                        <p>
                            <ul>
                              <li>- To inform and discuss on healthy workplace dynamics, including gender mainstreaming in safety policies and practice</li>
                              <li>- Promote dynamic employee wellness in the workplace</li>
                              <li>- To empower the OSH Committee Members with new aspects of Occupational Safety and Health Act 2007.</li>
                              <li>- To impart and provide practical skills towards practical practices for effective OSH planning, organization, implementation, evaluation, hazard prevention, internal audits and other safe work practices.</li>
                            </ul>
                        </p>
                        <h5>Terms of Reference</h5>
                        <p>
                             We undertake to do the following:
                             <ul>
                              <li>- Conduct training of safety and health committee on occupational safety and health as per the DOSHS approved syllabus.</li>
                              <li>- Lead the committee members in carrying out workplace inspection</li>
                              <li>- Compile a detailed report on the training activity</li>
                              <li>- Prepare training materials (handout of training notes at the end of the training and other resources)</li>
                              <li>- Provide CERTIFICATES that are duly recognized and approved by the Directorate of Occupational Health and Safety Services to all course participants upon successful completion of training.</li>
                            </ul>

                        </p>
                        <h4>b.First Aid Training</h4>
                        <p>
                           PLP Project EHS (Environmental, Health & Safety) first aid training provides competent first-aid skills to the workplace first aid team. The team is thoroughly equipped with procedures, 
                           skills, and knowledge to address possible first aid cases and emergecies that might arise in the workplace. Objectives of the training are;
                           <ul>
                              <li>- To safe life.</li>
                              <li>- To prevent further injury.</li>
                              <li>- To promote the recovery from injury sustained.</li>                              
                            </ul>
                            The training can be carried out for all or for a specific group of your employees, or for a specific department as you may desire. The training 
                            can be carried out in-house or any other venue as may be agreed upon.
                        </p>
                        <h5>Terms of reference</h5>
                        <p>
                        The PLP Project EHS (Environmental, Health & Safety) first aid training team will undertake to do the following:
                        <ul>
                              <li>- Conduct training of the first aid as per the DOSHS first aid training approved syllabus.</li>
                              <li>- Provide CERTIFICATES that are duly recognized and approved by the Directorate of Occupational Health and Safety Services to all course participants upon successful completion of training.</li>                             
                            </ul>
                        </p>
                        <h4>c.Fire Fighting Training</h4>
                        <p>
                        We conduct fire safety training at your premises for your fire fighting team. The law requires that every workplace has in place a fire safety 
                        team which should be trained on fire safety by DOSHS approved fire safety trainers in line with sec. 20 and 21 of the Fire Risk Reduction Rules, 
                        2007. The training will be conducted by our experienced and registered fire safety trainers at your premises. The training includes, but not 
                        limited to, the following topics: 
                        <ul>
                              <li>- Basics of fire and the fire triangle.</li>
                              <li>- Causes of fire.</li>
                              <li>- Prevention of fire</li> 
                              <li>- Fire prevention policy</li>
                              <li>- Ways of controlling the spread of fire</li>
                              <li>- Classes of fire.</li>  
                              <li>- Fire fighting equipments.</li> 
                              <li>- Emergency response preparedness and evacuation procedures/Emergency response plans.</li> 
                              <li>- Demonstration/ Fire Drill</li>                          
                            </ul>                            
                        </p>
                        <h5>FIRE SAFETY TRAINING BENEFITS</h5>
                        <p>
                             In the end, Staff will be more conscious about fire safety and will contribute to the following : 
                             <ul>
                              <li>- Better management of fire risks and ultimately reduce fire incidences now and in the future. </li>
                              <li>- Proper management of fire incidences.</li>
                              <li>- Competent and skilled fire response and emergency evacuation. </li> 
                              <li>- Well equipped staff on usage of fire suppression equipments.</li> 
                              <li>- Finally, the fire safety training will demonstrate the company’s commitment towards compliance with legal and regulatory Statutories.</li>                           
                            </ul> 
                        </p>
                        <h5>TERMS Of REFERENCE</h5>
                        <p>
                           PLP Project EHS (Environmental, Health & Safety) fire safety training team will undertake to do the following: 
                           <ul>
                              <li>- Conduct Professional Fire safety training to staff on DOSHS and DIT approved syllabus.</li>
                              <li>- Provide CERTIFICATES that are duly recognized and approved by the Directorate of Occupational Health and Safety Services to all course participants upon successful completion of training.</li>                         
                            </ul>                              
                        </p>
                        <h4>d.Environmental Awareness and Training</h4>
                        <p>
                        PLP Project EHS (Environmental, Health & Safety)'s training team conducts environmental awareness that is geared towards equipping your staff with best environmental skills and knowledge. Training 
                        staff in environmental awareness is expected to greatly assist in developing their capacity and improve their sense of organizational environmental 
                        and safety awareness. The training will also engender the culture whereby all staff can take personal responsibility for their environment and safety in all 
                        that they do. <br> Training Objectives include;
                        <ul>
                              <li>- Understand current environmental regulations and how they are applicable to organizations.</li>
                              <li>- Understand organization environmental significant aspects and impacts.</li>
                              <li>- Understand Environmental Management Plan(EMP).</li> 
                              <li>- Learn an overview of ISO 14001</li>                            
                            </ul>                                                                                                        
                        </p>
                        <h4>e.HIV/AIDS Training</h4>
                        <p>
                        PLP Project EHS (Environmental, Health & Safety)’s objective of HIV/AIDS training is to offer a comprehensive training geared towards a better understanding of the subject and proper management 
                        of HIV/ AIDS victims and cases in the workplace as per Ministry of Labor regulations . Training Scope includes
                        <ul>
                              <li>- Sensitize and train staff on the psychosocial and organizational effects of HIV/ AIDS.</li>
                              <li>- Sensitize on the management of HIV/ AIDS cases and workers at the workplace.</li>
                              <li>- Prepare training materials (handouts and other resources) for the training.</li> 
                              <li>- Provide CERTIFICATES to the course participants.</li> 
                              <li>- Provide a training report for the management, supervisors and general workers once sensitization is completed.</li>                           
                            </ul>                                                                                                        
                        </p>

